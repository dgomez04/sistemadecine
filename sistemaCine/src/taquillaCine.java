    private void continuarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continuarButtonActionPerformed
        // Anadir un elemento a la lista.
        String nombre = peliculasComboBox.getSelectedItem().toString();
        String horario = horarioComboBox.getSelectedItem().toString();
        String sala = salaPane.getText();
        int boletos = (Integer) menorSpinner.getValue() + (Integer) adultosSpinner.getValue();
        Double total = Double.valueOf((Integer) menorSpinner.getValue() * 5) + ((Integer) adultosSpinner.getValue() * 7.25);
        Double precioFinal = (total) + (total * 0.13);
       
        cobro.setTitle("Caja");
      
        //agrega la informacion y utiliza la cola para mantener orden en la fila.
        if(cobro.cola.cantidad() < 1) {
            
            //agregar a la lista
            lista.agregar(nombre, horario, sala, boletos, total);
            lista.imprimir();
            
            //agregar a la pila
            ventas.add(new ventasTotales(boletos, precioFinal));
            
            //agregar a la cola 
            cobro.cola.insertar(lista.orderNumber());
            cobro.ordernumPanel.setText(String.valueOf(lista.orderNumber()));
            cobro.cantPanel.setText(String.valueOf(boletos));
            cobro.totalOrden.setText(String.valueOf(precioFinal));
        } else {
            System.out.println(" ");
            System.out.println("Porfavor a esperar a que terminemos de procesar la siguiente transaccion. ");
        }

        cobro.setVisible(true);
        String control = "n";
        
        
        
      
        
        
        
    }//GEN-LAST:event_continuarButtonActionPerformed

 
    
    
    
    
