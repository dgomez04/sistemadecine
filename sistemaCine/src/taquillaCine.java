    private void ventasButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ventasButtonActionPerformed
        // TODO add your handling code here:
        int boletosTotales = 0;
        double ventasFinal = 0.0;
        
        for(ventasTotales venta : ventas) {
            //suma todos los boletos
            boletosTotales += venta.getBoletos();
            
            //suma el total de las ventas
            ventasFinal += venta.getTotalVenta();
        }
        
        System.out.println("\nEl dia de hoy, se han vendido " + boletosTotales + " boletos por un total de $" + ventasFinal);
        System.out.println("\nLa ultima venta realizada es de " + ventas.peek().getBoletos() + " boletos por un total de $" + ventas.peek().getTotalVenta());
        
        
        
    }//GEN-LAST:event_ventasButtonActionPerformed

